    public static string Get(string culture, string key)
    {
        var dictionary = GetDictionary(culture);
        return dictionary.TryGetValue(key, out var value) ? value : key;
    }

    private static Dictionary<string, string> GetDictionary(string culture)
    {
        if (culture.StartsWith("en", StringComparison.OrdinalIgnoreCase))
        {
            return English;
        }

        if (culture.StartsWith("ja", StringComparison.OrdinalIgnoreCase))
        {
            return Japanese;
        }

        return Russian;
    }
